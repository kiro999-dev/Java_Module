        fileTypes.put("GIF89a", new byte[]{0x47,0x49,0x46,0x38,0x39,0x61});
        fileTypes.put("PDF", new byte[]{0x25,0x50,0x44,0x46});
        fileTypes.put("ZIP", new byte[]{0x50,0x4B,0x03,0x04});
        fileTypes.put("RAR", new byte[]{0x52,0x61,0x72,0x21,0x1A,0x07});
        fileTypes.put("GZIP", new byte[]{0x1F, (byte)0x8B});
